print ('Porfavor, insira as seguintes informações para o calculo da área do retângulo: \n')

base_retangulo = float(input('Valor da base do retângulo(em centimetros): '))
altura_retangulo = float(input('Valor da altura do retângulo(em centimetros): '))

area_retangulo = base_retangulo * altura_retangulo

print(f'Área do retângulo é de {area_retangulo} cm².\n')